#ifndef STRING_H
#define STRING_H

/**
 * strlen - get the length of a string
 * @param *str string to get the length from
 * @return the actual string length
 */
int strlen(char *str);

/**
 * strreverse - reverse the string
 * @param *str string to reverse
 */
void strreverse(char *str);

#endif  // !STRING_H